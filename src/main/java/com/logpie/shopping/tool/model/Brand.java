package com.logpie.shopping.tool.model;

import com.logpie.framework.db.annotation.DatabaseColumn;
import com.logpie.framework.db.annotation.DatabaseColumn.ColumnType;
import com.logpie.framework.db.annotation.DatabaseEntity;
import com.logpie.shopping.tool.repository.BrandRepository;

@DatabaseEntity(table = BrandRepository.DB_TABLE_NAME_BRAND)
public class Brand extends LogpieModel {

	private String mBrandId;
	private String mBrandName;

	/**
	 * Constructor for creating a brand
	 * 
	 * @param brandName
	 */
	public Brand(final String brandName) {
		this.mBrandName = brandName;
	}

	/**
	 * 
	 * @param brandId
	 * @param brandName
	 */
	public Brand(final String brandId, final String brandName) {
		this.mBrandId = brandId;
		this.mBrandName = brandName;
	}

	@DatabaseColumn(name = BrandRepository.DB_KEY_BRAND_ID, type = ColumnType.LONG, autoGenerated = true, primaryKey = true)
	public String getBrandId() {
		return mBrandId;
	}

	@DatabaseColumn(name = BrandRepository.DB_KEY_BRAND_NAME, type = ColumnType.STRING)
	public String getBrandName() {
		return mBrandName;
	}

	public void setBrandName(final String brandName) {
		this.mBrandName = brandName;
	}
}
