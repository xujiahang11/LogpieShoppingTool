package com.logpie.shopping.tool.model;

import com.logpie.framework.db.annotation.DatabaseColumn;
import com.logpie.framework.db.annotation.DatabaseColumn.ColumnType;
import com.logpie.framework.db.annotation.DatabaseEntity;
import com.logpie.shopping.tool.repository.SubCategoryRepository;

@DatabaseEntity(table = SubCategoryRepository.DB_TABLE_NAME_SUBCATEGORY)
public class SubCategory extends LogpieModel {
	private String mSubCategoryId;
	private String mSubCategoryName;
	private Category mCategory;

	/**
	 * Constructor for creating a sub-category
	 * 
	 * @param subCategoryName
	 */
	public SubCategory(final String subCategoryName, final Category category) {
		this.mSubCategoryName = subCategoryName;
		this.mCategory = category;
	}

	/**
	 * 
	 * @param subCategoryId
	 * @param subCategoryName
	 */
	public SubCategory(final String subCategoryId,
			final String subCategoryName, final Category category) {
		this.mSubCategoryId = subCategoryId;
		this.mSubCategoryName = subCategoryName;
		this.mCategory = category;
	}

	@DatabaseColumn(name = SubCategoryRepository.DB_KEY_SUBCATEGORY_ID, type = ColumnType.LONG, autoGenerated = true, primaryKey = true)
	public String getSubCategoryId() {
		return mSubCategoryId;
	}

	@DatabaseColumn(name = SubCategoryRepository.DB_KEY_SUBCATEGORY_NAME, type = ColumnType.STRING)
	public String getSubCategoryName() {
		return mSubCategoryName;
	}

	@DatabaseColumn(name = SubCategoryRepository.DB_KEY_CATEGORY_ID, type = ColumnType.LONG)
	public String getCategoryId() {
		return mCategory.getCategoryId();
	}

	public Category getCategory() {
		return mCategory;
	}

	public void setSubCategoryName(String subCategoryName) {
		this.mSubCategoryName = subCategoryName;
	}

	public void setCategory(Category category) {
		this.mCategory = category;
	}

}
